function writeStudentsBatch() {
    var text = '';
    for (var i = 0; i <= studentNames.length - 1; i++) {
        text+= '<div class="grid-item"><a href="student_info.html" onclick="saveStudent(' + i + ')""><img src="';
        text+= studentNames[i][2]; //image
        text+= '"></a>\n<h2><a href="student_info.html" onclick="saveStudent(' + i + ')"">';
        text+= studentNames[i][0]; //name
        text+= '</a></h2>\n<h3>';
        text+= studentNames[i][1]; //major
        text+= '</h3></div>';
    };
    return text;
}

function saveStudent(i) {
    localStorage.setItem("designatedStudent", i);  
}

/*
THE BELOW FUNCTIONS ARE FOR THE STUDENT INFO PAGE
*/

var designatedStudent = parseInt(localStorage.getItem('designatedStudent'));

function getStudentInfoName() {
    return studentNames[designatedStudent][0];
}

function getStudentInfoMajor() {
    return studentNames[designatedStudent][1];
}

function getStudentInfoImg() {
    text = '<img src="' + studentNames[designatedStudent][2] + '">';
    return text;
}

function getStudentInfoPortfolio() {
    text = '<a href="' + studentNames[designatedStudent][3] + '">Portfolio</a>';
    return text;
}

function printStudentSidebar() {
    text = '<a href="index.html" style="font-size: 130%">Index</a>'
    for (var i = 0; i <= studentNames.length - 1; i++) {
        text += '<hr><a href="student_info.html" onclick="saveStudent(' + i + ')"">';
        text += studentNames[i][0]; //name
        text += '</a>';
    };
    return text;
}